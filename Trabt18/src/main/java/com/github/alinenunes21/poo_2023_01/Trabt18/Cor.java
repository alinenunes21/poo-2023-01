public class Cor {
    private String corPreto;
    private String corBranca;

    public Cor(String corPreto, String corBranca) {
        this.corPreto = corPreto;
        this.corBranca = corBranca;
    }

    public String getCorPreto() {
        return corPreto;
    }

    public String getCorBranca() {
        return corBranca;
    }

}
