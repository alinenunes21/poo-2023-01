package com.github.alinenunes21.poo_2023_01.t19.dia;

public class Dia {
    private int dia;
    private int mes;
    public Dia(int dia, int mes) {
        this.dia = dia;
        this.mes = mes;
    }

    public int getDia() {
        return dia;
    }

    public void setDia(int dia) {
        this.dia = dia;
    }

    public int getMes() {
        return mes;
    }
    public void setMes(int mes) {
        this.mes = mes;
    }
}
