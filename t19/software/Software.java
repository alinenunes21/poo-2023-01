package com.github.alinenunes21.poo_2023_01.t19.software;

public class Software {

    private String software;
    private Versao versaoAtual;

    public Software(String nome, Versao versaoAtual) {
        this.software = software;
        this.versaoAtual = versaoAtual;
    }

    public String getNome() {
        return software;
    }

    public void setNome(String software) {
        this.software = software;
    }

    public Versao getVersaoAtual() {
        return versaoAtual;
    }

    public void setVersaoAtual(Versao versaoAtual) {
        this.versaoAtual = versaoAtual;
    }
}
